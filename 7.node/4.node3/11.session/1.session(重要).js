/* 
关于session：一定要记住
  1.是什么?
    标准来说，session这个单词指的是会话。
    1.1 前端通过浏览器去查看cookie的时候，会发现有些cookie的过期时间是：session，意味着该cookie是会话cookie
    1.2 后端人员常常把【session会话存储】简称为session存储，或者更简单的称为：session

  2.特点;
    2.1 存在于服务端
    2.2 存储的是浏览器和服务器之间沟通产生的一些信息

  3.默认session存储在服务器的内存中，每当一个新客户端发来请求，服务器都会新开辟出一块空间，供session会话存储使用。

  4.工作流程:
    --第一次浏览器请求服务器的时候，服务器会开辟出一块内存空间，供session会话存储使用。
    --返回响应的时候，会自动返回一个cookie（有时候会返回多个，为了安全〉，cookie里包含着，上一步产生会话存储“容器”的编号（id）
    --以后请求的时候，会自动携带这个cookie，给服务器。
    --服务器从该cookie中拿到对应session的id，去服务器中匹配。
    --服务器会根据匹配信息，决定下一步逻辑。

   5.备注：
    5.1 一般来说cookie一定会配合session使用。                -
    5.2 服务端一般会做session的持久化，防止山由于服务器重启，造成session的丢失。
*/